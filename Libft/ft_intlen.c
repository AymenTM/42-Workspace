#include "libft.h"

unsigned int	ft_intlen(long n)
{
	unsigned int i;

	i = 0;
	if (n < 0)
	{
		i++;
		n = -n;
	}
	while (n > 0)
	{
		i++;
		n /= 10;
	}
	return (i);
}
