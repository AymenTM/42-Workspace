unsigned int	ft_alpha_to_index(char c)
{
	if (c >= 'a' && c <= 'z')
	{
		return (c - 97);
	}
	else if (c >= 'A' && c <= 'Z')
	{
		return (c - 65);
	}
	else if (c == '\'')
	{
		return (26);
	}
	return (0);
}
