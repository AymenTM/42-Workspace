/*
**    NAME
**         list_map -- create a new list out of mapped element of another list.
**
**    SYNOPSIS
**         #include <../libft.h>
**
**         int
**         list_map(t_lst **dest, t_lst *src, t_lst *(*f)(t_lst *elem));
**
**    PARAMETERS
**
**         t_lst **dest                   Pointer to the destination pointer
**                                         of the new list.
**
**         t_lst *src                     Pointer to the head of the source
**                                         list.
**
**         t_lst *(*f)(t_lst *elem)      Mapping function that takes a list
**                                         elements, and returns a different
**                                         list element.
**
**    DESCRIPTION
**         Creates a new list out of mapped elements from the 'src' list,
**         storing the head of the new list in (*dest). The mapped elements
**         are generated by the mapping function 'f()'.
**
**    RETURN VALUES
**         Returns 0 if successful; otherwise -1.
*/

#include "../libft.h"

int		list_map(t_lst **dest, t_lst *src, t_lst *(*f)(t_lst *elem))
{
	t_lst	*new_elem;
	t_lst	*previous;

	if (src && *f)
	{
		(*dest) = (*f)(src);
		previous = (*dest);
		src = src->next;
		while (src)
		{
			new_elem = (*f)(src);
			previous->next = new_elem;
			previous = new_elem;
			src = src->next;
		}
		previous->next = NULL;
		return (0);
	}
	return (-1);
}
