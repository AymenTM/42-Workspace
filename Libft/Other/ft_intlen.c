#define INT_MIN -2147483648

unsigned int	ft_intlen(int n)
{
	unsigned int i;

	i = 0;
	if (n == INT_MIN)
		return (11);
	if (n < 0)
	{
		i++;
		n = -n;
	}
	while (n > 0)
	{
		i++;
		n /= 10;
	}
	return (i);
}
